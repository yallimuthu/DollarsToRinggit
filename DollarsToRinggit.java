public class DollarsToRinggit {
    public static void main(String[] args) {
        double dollars;
        double ringgit;

        dollars = 2.00;
    
        // convert U.S Dollar to ringgit (This is the current conversion rate)
        ringgit = (dollars * 4.30);

        System.out.print(
            dollars + " U.S Dollar is equal to " + ringgit + " Malaysia ringgit.");
    }    
}